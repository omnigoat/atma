#ifndef ATMA_ASSERT_ENSURE_HPP
#define ATMA_ASSERT_ENSURE_HPP
//=====================================================================
	
	//=====================================================================
	// ATMA_ENSURE
	// -------------
	//   halts if the boolean expression @x evaluates to false. always.
	//   even in release builds.
	//=====================================================================
	#define ATMA_ENSURE(x) \
		do { \
			if ( !(x) ) \
				{ __asm int 3 } \
		} while (0)


	//=====================================================================
	// ATMA_ENSURE_IS
	// ----------------
	//   ensures that the expression @x evaluates to @r
	//=====================================================================
	#define ATMA_ENSURE_IS(r, x) \
		ATMA_ENSURE((r) == (x))

//=====================================================================
#endif // inclusion guard
